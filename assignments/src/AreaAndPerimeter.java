public class AreaAndPerimeter {

	public static void main(String[] args) {
		//assign width
		double width = 5.5;
		
		//assign height
		double height = 8.5;
		
		//calculate perimeter
		double perimeter = 2 * width + 2 * height;
		
		//calculate area
		double area = width * height;
		
		//print result
		System.out.println("Perimeter of a rectangel with width: "+
		width + " and height "+ height + " is: "+ perimeter);
		
		System.out.println("Area of a rectangel with width: "+
				width + " and height "+ height + " is: "+ area); 

	}

}
